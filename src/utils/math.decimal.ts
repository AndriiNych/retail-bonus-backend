export const MATH = {
  DECIMAL: {
    add: (value1: string, value2: string) => {
      const result = parseFloat(value1) + parseFloat(value2);

      return result.toString();
    },
    subtract: (value1: string, value2: string) => {
      const result = parseFloat(value1) + parseFloat(value2);

      return result.toString();
    },
  },
};
